    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            username = txtUsername.getText();
            password = txtPassword.getText();
            String sql = "SELECT * FROM users WHERE username=? and password=?";
            PreparedStatement pst = con.prepareCall(sql);
            pst.setString(1, username);
            pst.setString(2, password);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Username and Password matched");
                pst = con.prepareStatement("Update users set online=? WHERE username=?");
                pst.setInt(1, online);
                pst.setString(2, username);
                pst.executeUpdate();
                
                setVisible(false);
                cr.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Username and Password do not match");
                txtUsername.setText("");
                txtPassword.setText("");
            }
        } catch (HeadlessException | SQLException e) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String username, password;
            username = txtUsername.getText();
            password = txtPassword.getText();

            String sql = "SELECT * FROM users WHERE username=?";
            PreparedStatement pst = con.prepareCall(sql);
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This username has already taken!");
            } else {
                pst = con.prepareStatement("insert into users (username,password,online)values(?,?,?)");
                pst.setString(1, username);
                pst.setString(2, password);
                pst.setInt(3, online);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Register successfully");

                txtUsername.setText("");
                txtPassword.setText("");
                setVisible(false);
                cr.setVisible(true);

                txtUsername.requestFocus();
            }

        } catch (SQLException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

